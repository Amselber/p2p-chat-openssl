// src/transport.h
#ifndef TRANSPORT_H
#define TRANSPORT_H
#include <stdint.h>

/**
 * @file transport.h
 * @brief TCP + TLS + отправка/получение текстовых строк
 *
 * Модуль предоставляет защищённый транспортный уровень поверх TCP.
 *
 * ## Архитектура
 *
 * Один контекст SSL_CTX на весь процесс. Для каждого соединения
 * создаётся SSL-объект. Массив g_ssl[fd] хранит SSL* для каждого
 * файлового дескриптора.
 *
 * Если g_ssl[fd] != NULL — соединение защищено TLS.
 * Если g_ssl[fd] == NULL — обычный TCP (только для отладки).
 *
 * ## Порядок использования
 *
 * **Старт:**
 * 1. transport_tls_init() — загрузить сертификаты, создать контекст
 * 2. transport_listen() — начать слушать порт для входящих соединений
 *
 * **Исходящее соединение:**
 * 3. transport_connect() — установить TCP-соединение
 * 4. transport_tls_connect() — выполнить TLS-рукопожатие (клиент)
 * 5. transport_peer_fingerprint() — проверить fingerprint сертификата
 * 6. transport_send() / transport_recv() — обмен сообщениями
 *
 * **Входящее соединение:**
 * 3. transport_accept() — принять TCP-соединение
 * 4. transport_tls_accept() — выполнить TLS-рукопожатие (сервер)
 * 5. transport_peer_name() — получить имя пира из сертификата
 * 6. transport_send() / transport_recv() — обмен сообщениями
 *
 * **Завершение:**
 * - transport_tls_close() + transport_close() — закрыть соединение
 * - transport_tls_cleanup() — освободить контекст (при выходе)
 *
 * ## Формат сообщений
 *
 * Каждое сообщение — текстовая строка, завершающаяся символом `\n`.
 * Отправка и приём обрабатывают сообщения целиком.
 *
 * ## Безопасность
 *
 * - Взаимная TLS-аутентификация (mTLS): обе стороны предъявляют сертификаты
 * - Сертификаты должны быть подписаны одним корневым CA
 * - После TLS-рукопожатия канал шифруется (AES-GCM или аналогично)
 * - Fingerprint = SHA256(DER-кодировка сертификата) для криптографической
 *   привязки HELLO-пакета к сертификату
 */

/* ──────────────────────────────────────────────
 * Инициализация и завершение
 * ────────────────────────────────────────────── */

/**
 * @brief Инициализация TLS-контекста
 *
 * Загружает корневой сертификат, свой сертификат и приватный ключ.
 * Включает режим взаимной проверки сертификатов (mTLS).
 * Вычисляет fingerprint своего сертификата.
 *
 * Должна быть вызвана один раз при старте программы,
 * до любых других функций модуля.
 *
 * @param ca_path   Путь к файлу корневого сертификата (rootCA.crt)
 * @param cert_path Путь к файлу своего сертификата (client.crt)
 * @param key_path  Путь к файлу своего приватного ключа (client.key)
 * @return 0 при успехе, -1 при ошибке
 */
int transport_tls_init(const char *ca_path, const char *cert_path,
                       const char *key_path);

/**
 * @brief Освобождение всех ресурсов TLS
 *
 * Закрывает все активные TLS-соединения и освобождает контекст.
 * Должна быть вызвана при завершении программы.
 */
void transport_tls_cleanup(void);

/**
 * @brief Свой fingerprint сертификата
 *
 * Возвращает SHA256 своего сертификата в виде hex-строки
 * из 64 символов. Используется в HELLO-пакетах для идентификации.
 *
 * @return Строка из 64 hex-символов (статический буфер)
 */
const char *transport_self_fingerprint(void);

/* ──────────────────────────────────────────────
 * TCP-соединения
 * ────────────────────────────────────────────── */

/**
 * @brief Создать слушающий TCP-сокет
 *
 * Создаёт сокет, привязывает к порту и переводит в режим
 * прослушивания входящих соединений.
 *
 * @param[in,out] port Если 0 — ОС выберет свободный порт.
 *                     Реальный назначенный порт записывается в *port.
 * @return fd сокета (>= 0) или -1 при ошибке
 */
int transport_listen(uint16_t *port);

/**
 * @brief Принять входящее TCP-соединение
 *
 * Извлекает первое соединение из очереди ожидающих.
 * Вызывается когда epoll сообщает о готовности listen_fd.
 *
 * @param listen_fd Файловый дескриптор слушающего сокета
 * @return fd для общения с клиентом (>= 0) или -1 при ошибке
 */
int transport_accept(int listen_fd);

/**
 * @brief Установить TCP-соединение с удалённым узлом
 *
 * Подключается к указанному IP и порту.
 * Вызов блокирующий — ждёт установки соединения.
 *
 * @param ip   IP-адрес (строка, например "192.168.1.5")
 * @param port TCP-порт
 * @return fd соединения (>= 0) или -1 при ошибке
 */
int transport_connect(const char *ip, uint16_t port);

/**
 * @brief Закрыть TCP-сокет
 *
 * Для TLS-соединений нужно предварительно вызвать transport_tls_close().
 *
 * @param fd Файловый дескриптор для закрытия
 */
void transport_close(int fd);

/* ──────────────────────────────────────────────
 * TLS-рукопожатие
 * ────────────────────────────────────────────── */

/**
 * @brief Выполнить TLS-рукопожатие (серверная сторона)
 *
 * Вызывается после transport_accept().
 * Проверяет сертификат клиента через verify_callback.
 * При неудаче соединение следует закрыть.
 *
 * @param plain_fd Файловый дескриптор TCP-соединения
 * @return 0 при успехе, -1 при ошибке
 */
int transport_tls_accept(int plain_fd);

/**
 * @brief Выполнить TLS-рукопожатие (клиентская сторона)
 *
 * Вызывается после transport_connect().
 * Проверяет сертификат сервера через verify_callback.
 * При неудаче соединение следует закрыть.
 *
 * @param plain_fd Файловый дескриптор TCP-соединения
 * @return 0 при успехе, -1 при ошибке
 */
int transport_tls_connect(int plain_fd);

/**
 * @brief Закрыть TLS-соединение
 *
 * Отправляет пиру close_notify и освобождает SSL-объект.
 * После вызова можно закрывать TCP-сокет через transport_close().
 *
 * @param fd Файловый дескриптор TLS-соединения
 */
void transport_tls_close(int fd);

/* ──────────────────────────────────────────────
 * Информация о пире (доступна после TLS-рукопожатия)
 * ────────────────────────────────────────────── */

/**
 * @brief Получить fingerprint сертификата пира
 *
 * Вычисляет SHA256 от DER-кодировки сертификата, предъявленного
 * пиром при TLS-рукопожатии.
 *
 * Используется для сверки с fingerprint из HELLO-пакета:
 * если HELLO утверждает одно, а сертификат содержит другое —
 * соединение разрывается (защита от MitM).
 *
 * @param fd Файловый дескриптор TLS-соединения
 * @return Строка из 64 hex-символов или NULL при ошибке
 */
const char *transport_peer_fingerprint(int fd);

/**
 * @brief Получить имя пира из сертификата
 *
 * Извлекает Common Name (CN) из поля Subject сертификата пира.
 * Используется для отображения имени в пользовательском интерфейсе.
 *
 * @param fd Файловый дескриптор TLS-соединения
 * @return Имя пира (статический буфер) или NULL при ошибке
 */
const char *transport_peer_name(int fd);

/* ──────────────────────────────────────────────
 * Обмен сообщениями
 * ────────────────────────────────────────────── */

/**
 * @brief Отправить текстовое сообщение
 *
 * Отправляет строку, добавляя `\n` как разделитель.
 * Если fd привязан к TLS — данные шифруются.
 * Если нет — отправляются открытым текстом (для отладки).
 *
 * @param fd   Файловый дескриптор соединения
 * @param text Текст для отправки (без `\n`)
 * @return 0 при успехе, -1 при ошибке или закрытии соединения
 */
int transport_send(int fd, const char *text);

/**
 * @brief Получить текстовое сообщение
 *
 * Читает данные по одному байту до разделителя `\n`.
 * Возвращает строку без завершающего `\n`.
 *
 * В неблокирующем режиме возвращает NULL если данных нет (EAGAIN).
 * При закрытии соединения пиром возвращает NULL
 * (предварительно вернув остаток данных если они были).
 *
 * @param fd Файловый дескриптор соединения
 * @return Указатель на статический буфер с сообщением,
 *         или NULL если данных нет / соединение закрыто
 */
const char *transport_recv(int fd);

#endif
